    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        //Returns the selected row number
        int rowForDeletion = VehicleTable.getSelectedRow();
        //Retrieves the Task Number from that row which is the Primary key for the work_order table in the database
        String vinString = (VehicleTable.getValueAt(rowForDeletion, 0).toString());
        
        //Removing the vehicle and its associated appointments from the database
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb?useSSL=false", "root", "password");
            //First we must remove the work orders associated with the vehicle
            String deleteWOSQL = "DELETE FROM work_order WHERE Vehicle IN (SELECT VIN from vehicle WHERE VIN = ?)";
            PreparedStatement deleteWOStmt = con.prepareStatement(deleteWOSQL);
            deleteWOStmt.setString(1, vinString);
            int workOrderRowAffected = deleteWOStmt.executeUpdate();
            
            //Then we can remove the requested vehicle from the vehicle table
            String removeVehicleQuery = "DELETE FROM vehicle WHERE VIN = ?";
            PreparedStatement deleteVehStatement = con.prepareStatement(removeVehicleQuery);
            deleteVehStatement.setString(1, vinString);
            int vRowsAffected = deleteVehStatement.executeUpdate();

            if (vRowsAffected > 0 || workOrderRowAffected > 0) {
                JOptionPane.showMessageDialog(this, "The Vehicle and Associated Appointments have been deleted.");
            } else {
                JOptionPane.showMessageDialog(this, "No matching appointment found.");
            }
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

