    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb?useSSL=false","root","password");

            DefaultTableModel tblModel = (DefaultTableModel)VehicleTable.getModel();
            tblModel.setRowCount(0);
            Statement st = con.createStatement();
            String sql = "select * from vehicle";
            ResultSet rs = st.executeQuery(sql);

            while(rs.next()){
                String vin = rs.getString("VIN");
                String year = rs.getString("Year");
                String make = rs.getString("Make");
                String model = rs.getString("Model");
                String miles = String.valueOf(rs.getInt("Miles"));
                String owner = rs.getString("Owner");

                String tbData[] = {vin,year,make,model,miles,owner};

                tblModel.addRow(tbData);
            }

        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

