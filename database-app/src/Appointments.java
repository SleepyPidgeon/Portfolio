    private void DeleteAppointmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteAppointmentActionPerformed
        // TODO add your handling code here:
        //Returns the selected row number
        int rowForDeletion = AppointmentTable.getSelectedRow();
        //Retrieves the Task Number from that row which is the Primary key for the work_order table in the database
        String taskNoString = (AppointmentTable.getValueAt(rowForDeletion, 0).toString());
        //Convert string to int, also -1 due to table in
        int taskNoForDeletion = Integer.parseInt(taskNoString);
        
        //Removing the work_order from the database
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb?useSSL=false", "root", "password");
            String sql = "DELETE FROM work_order WHERE TASKNO = ?";
            PreparedStatement preparedStatement = con.prepareStatement(sql);
            preparedStatement.setInt(1, taskNoForDeletion);
            int rowsAffected = preparedStatement.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Appointment Deleted");
            } else {
                JOptionPane.showMessageDialog(this, "No matching appointment found.");
            }
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }

    }//GEN-LAST:event_DeleteAppointmentActionPerformed

    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        // TODO add your handling code here:
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb?useSSL=false","root","password");

            DefaultTableModel tblModel = (DefaultTableModel)AppointmentTable.getModel();
            tblModel.setRowCount(0);
            //The date entered in the text box
            String fDate = filterBox.getText();
            Statement st = con.createStatement();
            String sql = "SELECT wo.TaskNo, wo.Start_Date, tech.F_Name AS Technician_FirstName, serv.ServiceName, veh.VIN, cust.F_Name AS Customer_FirstName " +
                  "FROM work_order wo " +
                  "JOIN technician tech ON wo.Technician = tech.T_ID " +
                  "JOIN services serv ON wo.ServiceNo = serv.ServiceNo " +
                  "JOIN vehicle veh ON wo.Vehicle = veh.VIN " +
                  "JOIN customer cust ON veh.Owner = cust.C_ID " +
                  "WHERE wo.Start_Date = CAST('"+ fDate + "' AS DATE)";
            ResultSet rs = st.executeQuery(sql);

            while(rs.next()){
                String task = rs.getString("TaskNo");
                String startD = rs.getString("Start_Date");
                String technician = rs.getString("Technician_FirstName");
                String servName = rs.getString("ServiceName");
                String vehicle = rs.getString("VIN");
                String customer = rs.getString("Customer_FirstName");

                String tbData[] = {task,startD,technician,servName,vehicle,customer};

                tblModel.addRow(tbData);
            }
            //Clears the text box after execution
            filterBox.setText("");

        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_SearchButtonActionPerformed

