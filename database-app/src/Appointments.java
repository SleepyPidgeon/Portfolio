    private void LoadAllAppointmentsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoadAllAppointmentsActionPerformed
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb?useSSL=false","root","password");

            DefaultTableModel tblModel = (DefaultTableModel)AppointmentTable.getModel();
            tblModel.setRowCount(0);
            Statement st = con.createStatement();
            String sql = "SELECT wo.TaskNo, wo.Start_Date, tech.F_Name AS Technician_FirstName, serv.ServiceName, veh.VIN, cust.F_Name AS Customer_FirstName " +
                  "FROM work_order wo " +
                  "JOIN technician tech ON wo.Technician = tech.T_ID " +
                  "JOIN services serv ON wo.ServiceNo = serv.ServiceNo " +
                  "JOIN vehicle veh ON wo.Vehicle = veh.VIN " +
                  "JOIN customer cust ON veh.Owner = cust.C_ID";
            ResultSet rs = st.executeQuery(sql);

            while(rs.next()){
                String task = rs.getString("TaskNo");
                String startD = rs.getString("Start_Date");
                String technician = rs.getString("Technician_FirstName");
                String servName = rs.getString("ServiceName");
                String vehicle = rs.getString("VIN");
                String customer = rs.getString("Customer_FirstName");

                String tbData[] = {task,startD,technician,servName,vehicle,customer};

                tblModel.addRow(tbData);
            }

        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_LoadAllAppointmentsActionPerformed

