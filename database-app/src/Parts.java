    private void LoadAllPartsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoadAllPartsActionPerformed
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb?useSSL=false","root","password");

            DefaultTableModel tblModel = (DefaultTableModel)AppointmentTable.getModel();
            tblModel.setRowCount(0);
            Statement st = con.createStatement();
            String sql = "select * from parts";
            ResultSet rs = st.executeQuery(sql);

            while(rs.next()){
                String partNum = rs.getString("PartNo");
                String partName = rs.getString("PartName");
                String qty = rs.getString("Quantity");
                String cost = rs.getString("Cost");

                String tbData[] = {partNum,partName,qty,cost};

                tblModel.addRow(tbData);
            }

        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_LoadAllPartsActionPerformed

