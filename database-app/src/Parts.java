    private void AddPartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddPartActionPerformed
        // TODO add your handling code here:
        String pNum = PartNumBox.getText();
        String pName = PartNameBox.getText();
        String qty = PartQuantBox.getText();
        String cost = PartCostBox.getText();

        //Converts qty and cost to integer and double respectively
        int quant = Integer.parseInt(qty);
        double pCost = Double.parseDouble(cost);
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb?useSSL=false","root","password");
            
            String sql = "INSERT INTO parts VALUES ('" + pNum + "', '" + pName + "', '" + quant + "', '" + pCost + "')";

            //Insert the data into the table
            Statement s = con.createStatement();

            s.executeUpdate(sql);

            JOptionPane.showMessageDialog(this, "Part has successfully been added.");
            con.close();
            PartNumBox.setText("");
            PartNameBox.setText("");
            PartQuantBox.setText("");
            PartCostBox.setText("");
        }catch (Exception e){
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_AddPartActionPerformed

    private void UpdateQuantityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateQuantityActionPerformed
        // TODO add your handling code here:
        String pNum = PartNumQtyBox.getText();
        String pQty = QuantUpdateBox.getText();
        
        //Convert pQty to double
        int qty = Integer.parseInt(pQty);
        
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb?useSSL=false","root","password");
            
            String sql = "UPDATE parts "
                   + "SET Quantity = " + qty
                   + " WHERE PartNo = '" + pNum + "';";
            
            Statement s = con.createStatement();

            s.executeUpdate(sql);

            JOptionPane.showMessageDialog(this, "Part quantity has been updated.");
            PartNumQtyBox.setText("");
            QuantUpdateBox.setText("");
        }catch(Exception e){
            System.out.println("Error: "+e.getMessage());
        }
    }//GEN-LAST:event_UpdateQuantityActionPerformed

