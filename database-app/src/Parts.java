    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        // TODO add your handling code here:
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb?useSSL=false","root","password");

            DefaultTableModel tblModel = (DefaultTableModel)AppointmentTable.getModel();
            tblModel.setRowCount(0);
            //The part number entered in the text box
            String searchPart = searchBox.getText();
            Statement st = con.createStatement();
            //SQL Query to display searched item
            String sql = "SELECT *"
                    + "FROM parts "
                    + "WHERE PartNo = '" + searchPart + "'";
            ResultSet rs = st.executeQuery(sql);

            while(rs.next()){
                String pNo = rs.getString("PartNo");
                String pName = rs.getString("PartName");
                String qty = rs.getString("Quantity");
                String cost = rs.getString("Cost");
                

                String tbData[] = {pNo,pName,qty,cost};

                tblModel.addRow(tbData);
            }
            //Clears the text box after execution
            searchBox.setText("");

        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_SearchButtonActionPerformed

